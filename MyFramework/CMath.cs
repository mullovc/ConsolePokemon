using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyMath
{
    class CMath
    {

        public static int min(int a, int b)
        {
            return (a < b ? a : b);
        }
        public static int max(int a, int b)
        {
            return (a > b ? a : b);
        }

    }
}
